/**
 * initialise block
 *
 */
function crpTag_moduletagsblock_init()
{
	// Security
	pnSecAddSchema('Tagsmoduleblock::', 'Block title::');
}

/**
 * get information on block
 * 
 */
function crpTag_moduletagsblock_info()
{
	return array (
		'text_type' => 'crpTags module',
		'module' => 'crpTag',
		'text_type_long' => 'Collection of tags for a module',
		'allow_multiple' => true,
		'form_content' => false,
		'form_refresh' => false,
		'show_preview' => true
	);
}

/**
 * display block
 *
 * @param        array       $blockinfo     a blockinfo structure
 * @return       output      the rendered bock
 */
function crpTag_moduletagsblock_display($blockinfo)
{
	// security check
	if (!SecurityUtil :: checkPermission('Tagsmoduleblock::', "$blockinfo[title]::", ACCESS_READ))
		return;

	if (!pnModAvailable('crpTag'))
		return;

	// get the current language
	$currentlang = pnUserGetLang();

	// Break out options from our content field
	$vars = pnBlockVarsFromContent($blockinfo['content']);
	// get all module vars for later use
	$modvars = pnModGetVar('crpVideo');

	$apiargs['startnum'] = 1;
	$apiargs['numitems'] = $modvars['tag_itemsperpage'];
	$apiargs['extended'] = true;
	$apiargs['groupbyname'] = true;
	$apiargs['module'] = $vars['moduletags'];

	// call the api
	$items = pnModAPIFunc('crpTag', 'user', 'gettags', $apiargs);

	// check for an empty return
	if (empty ($items))
		return;

	// create the output object
	$pnRender = pnRender :: getInstance('crpTag', false);

	$pnRender->assign('tags', $items);
	$pnRender->assign('smodule', $vars['moduletags']);
	$pnRender->assign($modvars);
	
	$blockinfo['content'] = $pnRender->fetch('blocks/crptag_block_moduletags_cloud.htm');
		
	return pnBlockThemeBlock($blockinfo);
}

/**
 * modify block settings
 *
 * @param        array       $blockinfo     a blockinfo structure
 * @return       output      the bock form
 */
function crpTag_moduletagsblock_modify($blockinfo)
{
	// Break out options from our content field
	$vars = pnBlockVarsFromContent($blockinfo['content']);
	
	$modulesArray = array_keys(pnModAPIFunc('Modules','admin','gethookedmodules',array('hookmodname' => 'crpTag')));

	// Create output object
	$pnRender = pnRender :: getInstance('crpTag', false);

	// assign the block vars
	$pnRender->assign($vars);
	$pnRender->assign('modulesArray', $modulesArray);

	// Return the output that has been generated by this function
	return $pnRender->fetch('blocks/crptag_block_moduletags_cloud_modify.htm');
}

/**
 * update block settings
 *
 * @param        array       $blockinfo     a blockinfo structure
 * @return       $blockinfo  the modified blockinfo structure
 */
function  crpTag_moduletagsblock_update($blockinfo)
{
	// Get current content
	$vars = pnBlockVarsFromContent($blockinfo['content']);

	// alter the corresponding variable
	$vars['moduletags'] = FormUtil :: getPassedValue('moduletags', null);
	
	// write back the new contents
	$blockinfo['content'] = pnBlockVarsToContent($vars);

	// clear the block cache
	$pnRender = pnRender :: getInstance('crpTag');
	$pnRender->clear_cache('blocks/crptag_block_moduletags_cloud.htm');

	return $blockinfo;
}
